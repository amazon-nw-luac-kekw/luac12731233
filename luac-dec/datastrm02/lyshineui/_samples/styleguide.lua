function StyleGuide:GetImageList(stringList)
  function mysplit(inputstr)
    local sep = "\n"
    local t = {}
    for str in string.gmatch(inputstr, "([^" .. sep .. "]+)") do
      table.insert(t, str)
    end
    return t
  end
  local mysplitTable = mysplit(stringList)
  local fileNames = {}
  for i = 1, #mysplitTable do
    local val = mysplitTable[i]
    val = val:gsub("%s+", "")
    table.insert(fileNames, val)
  end
  return fileNames
end
