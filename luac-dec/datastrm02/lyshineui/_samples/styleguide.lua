function StyleGuide:OnCryAction(actionName, value)
  if actionName == "openStyleGuide" then
    self.isShowing = not self.isShowing
    UiCanvasBus.Event.SetEnabled(self.canvasId, self.isShowing)
  end
end
function StyleGuide:SetScreenState(state)
  if self.currentScreenState == state then
    return
  end
  if self.ScreenStates[state].screen then
    if self.currentScreen then
      self.previousScreen = self.currentScreen
      self.ScriptedEntityTweener:Play(self.currentScreen, 0.1, {
        opacity = 0,
        ease = "QuadOut",
        onComplete = function()
          UiElementBus.Event.SetIsEnabled(self.previousScreen, false)
        end
      })
    end
    if self.currentScreen == self.ScreenStates[self.STATE_COMPONENTS].screen then
      UiDropdownBus.Event.Collapse(self.Properties.ComponentDropdownSelected)
    end
    UiElementBus.Event.SetIsEnabled(self.ScreenStates[state].screen, true)
    self.ScriptedEntityTweener:Play(self.ScreenStates[state].screen, 0.3, {opacity = 0}, {opacity = 1, ease = "QuadOut"})
  end
  self.currentScreen = self.ScreenStates[state].screen
  self.currentScreenState = state
end
